-- DROP INDEX IF EXISTS public.users_api_token_uq;

CREATE UNIQUE INDEX IF NOT EXISTS users_api_token_uq
    ON public.users USING btree
    (api_token COLLATE pg_catalog."default" ASC NULLS LAST)
    TABLESPACE pg_default;